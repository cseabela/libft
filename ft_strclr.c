#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	int		z;

	if (!s)
		return ;
	i = 0;
	z = ft_strlen(s);
	while (s)
	{
		s[i] = '\0';
		if (i == z)
			return ;
		i++;
	}
}
